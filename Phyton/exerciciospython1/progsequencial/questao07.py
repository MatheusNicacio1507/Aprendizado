'''
 Fazer um algoritmo que pergunte 3 números e apresente a média aritmética entre estes 3 números
'''

num1 = int(input("Insira um valor: "))
num2 = int(input("Insira um valor: "))
num3 = int(input("Insira um valor: "))

media = (num1 + num2 + num3) / 3

print(f"A média entre esses números é = {media}")