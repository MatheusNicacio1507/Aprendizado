# conversão de tipos, coerção
# type convertion, typecasting, coercion
# é o ato de converter um tipo em outro
#  tipos imutáveis e primitivos:
# str, int, float, bool

print(int("1"))

print(type(int("1")))

print(int("1") + 1)
print(type(int("1") + 1))

print(bool("")) # Bool com nada denteo é False

print(bool(" ")) # Bool com alguma coisa dentro True

print(str(11) + "b") # Tem que ficar 11b
