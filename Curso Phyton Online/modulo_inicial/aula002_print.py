# Imprime as informações(números, variáveis, etc) e quebra linhas
print(12,34)
print(56,78)

# sep é usado para mudar o separador, ou seja, o espaço que a virgula do codigo faz. Ex:
print(910,1112, sep="-") # Bota o - no lugar do espaço

# end é usado para executar uma ação no final do print
print(1314,1516, sep="-", end="\n") # Bota o \n no final do código
print(1718,1920, sep="-", end="ABC\n") # Pode botar letras e números também
# \n quebra a linha dentro de um print